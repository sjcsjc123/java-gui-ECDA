package ownPanel.data;

public class CV {
    private String E;
    private String I;

    public CV(String e, String i) {
        E = e;
        I = i;
    }

    public String getE() {
        return E;
    }

    public void setE(String e) {
        E = e;
    }

    public String getI() {
        return I;
    }

    public void setI(String i) {
        I = i;
    }
}
